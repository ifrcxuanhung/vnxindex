class Shortcut extends Welcome {
    function __construct() {
        parent::__construct();
    }
    public function index($action="") {
        $this->db->where('group','shortcut');
        $shortcut = $this->db->get('setting')->result_array();
        foreach($shortcut as $value)
        {
            if($action == $value['key'])
            {
                $link = base_url() . $value['value'];
                echo "<script>window.location = '$link'</script>";
                exit;
            }
        }
        redirect(base_url() . 'home');
    }
}
