class Role_model extends CI_Model {

    var $table = 'role';
    var $table_group = 'group';

    function __construct() {
        parent::__construct();
    }

    
    public function find($id = NULL) {
        $this->db->select("$this->table_group.name as group_name, group_id, role_id, $this->table.description, $this->table.name");
        if (is_numeric($id)) {
            $this->db->where('role_id', $id);
        }
        $this->db->join($this->table_group, 'id = group_id', 'left');
        $query = $this->db->get($this->table);
        return $query->result_array();
    }

    /*     * ************************************************************** */
