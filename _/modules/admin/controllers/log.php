class Log extends Admin {

    function __construct() {
        parent::__construct();
        $this->load->model('log_model','Mlog');
    }

    
    function index() {
        $filename = './assets/log/user_log.txt';
        $this->Mlog->import_log($filename);
        $this->data->info = $this->Mlog->listLog();
        $this->template->write_view('content', 'log/index', $this->data);
        $this->template->write('title', 'log');
        $this->template->render();
    }
}
